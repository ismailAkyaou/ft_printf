#include "ft_printf.h"

int	ft_putunsigned(unsigned int n)
{
	unsigned int	i;

	i = 0;
	if (n > 9)
	{
		i = i + ft_putunsigned(n / 10);
		i = i + ft_putunsigned(n % 10);
	}
	else
		i = i + ft_putchar(n + 48);
	return (i);
}
