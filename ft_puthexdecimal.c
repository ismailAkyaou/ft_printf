#include "ft_printf.h"

int	ft_puthexdecimal(char d, unsigned int n)
{
	int		i;
	char	*base;

	base = "0123456789ABCDEF";
	if (d == 'x')
		base = "0123456789abcdef";
	i = 0;
	if (n >= 16)
		i += ft_puthexdecimal(d, n / 16);
	i += ft_putchar(base[n % 16]);
	return (i);
}
