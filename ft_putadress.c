#include "ft_printf.h"

int	ft_putadress(unsigned long n)
{
	int	i;

	i = 0;
	if (n >= 16)
	{
		i = i + ft_putadress(n / 16);
		i = i + ft_putadress(n % 16);
	}
	else if (n <= 9)
		i = i + ft_putchar(n + 48);
	else if (n <= 15)
		i = i + ft_putchar(n + 87);
	return (i);
}
